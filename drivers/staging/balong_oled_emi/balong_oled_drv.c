/*************************************************
*  Function:  oledClear
*  Description: oled clear *
*  Called By:AP
*  Table Accessed:
*  Table Updated:
*  Input:
*          UINT32 ulXStart, UINT32 ulYStart－－起始坐标
*          UINT32 ulXEnd, UINT32 ulYEnd－－横向纵向偏移量
*  Output:
*         N/A
*  Return:
*         N/A
*************************************************/
BSP_S32 oledClear(UINT32 ulXStart, UINT32 ulYStart,UINT32 ulXOffset, UINT32 ulYOffset)
{
	UINT32 ulRowAddr = 0x00;
	UINT32 ulXloop = 0x00;
	UINT32 ulYloop = 0x00;
	UINT32 ulLoopMax = 0x00;
	UINT32 ulXEnd = (ulXStart + ulXOffset);
	UINT32 ulYEnd = (ulYStart + ulYOffset);
	
	if ((ulXEnd > OLED_WIDTH) || (ulYEnd > OLED_HIGHT)  || (0 != (ulYStart % 8)) ||(0 != (ulYEnd % 8)))
	{
		hioled_error("oledClear para error!! ");
		return ERROR;
	}
	
	/*计算页起始地址0~7*/
	ulRowAddr = ulYStart >> 3;

	/*需添加互斥信号量保护*/
	down(&g_ulOledSemMux);
	
	/* Enable the EMI clock gating */
	/* clock_gating_enable(CLOCK_GATING_EMI); */

	/*closed display*/
	/*oledCmdOut(0xAE); */

	ulLoopMax = (ulYEnd>>3);
	for (ulYloop = ulRowAddr; ulYloop < ulLoopMax; ulYloop++)
	{
		/*设置页起始地址*/
		oledCmdOut(0x22);
		oledCmdOut((UINT8)ulYloop);
		oledCmdOut(0x07);
		
		/*设置列起始地址0~127*/
		oledCmdOut(0x21);
		oledCmdOut((UINT8)ulXStart);
		oledCmdOut(0x7f);
		for (ulXloop = ulXStart; ulXloop < ulXEnd; ulXloop++)
		{
			oledDataOut(0x00);
		}
	}
	
	/*output follows RAM content*/
	oledCmdOut(0xA4); 
	
	/*open display*/
	oledCmdOut(0xAF);
	
	/* disable the EMI clock gating */
	/* clock_gating_disable(CLOCK_GATING_EMI); */
	memset(buffer_t ,0 ,BUFFER_SIZE);
	up(&g_ulOledSemMux);
	
	return OK;   
}
