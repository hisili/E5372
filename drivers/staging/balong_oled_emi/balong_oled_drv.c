/*****************************************************************************
  Function    : oledCharDisplay
  Description : oledCharDisplay
  Calls       : 
  Called By   : 
  Input       : NULL
  Output      : NULL
  Return      : OK              ERROR      
*****************************************************************************/
BSP_VOID oledCharDisplay(UINT8 ucX, UINT8 ucY, UINT8 *pucChar)
{
	BSP_S32 ret = 0;
	UINT8 *pucStr = NULL;

	if ((*pucChar >= 'A' )&& (*pucChar <= 'Z' ))
	{
		pucStr = (UINT8 *)(g_aucStr_A_Z_8x16 + ((*pucChar -'A')*16));
	}
	else if ((*pucChar >= 'a' ) && (*pucChar <= 'z'))
	{
		pucStr = (UINT8 *)(g_aucStr_a_z_8x16 + ((*pucChar-'a')*16));
	}
	else if ((*pucChar >= '0' )&& (*pucChar <= '9'))
	{
		pucStr = (UINT8 *)(g_aucNum_0_9_8x16 + ((*pucChar-'0')*16));
	}
	else if (*pucChar == ' ')
	{
		pucStr = (UINT8 *) g_aucSpace;
	}
	else if (*pucChar == '!')
	{
		pucStr = (UINT8 *)g_aucExclamatory;
	}
	else
	{
	}

	ret = oledRefresh(ucX, ucY, 8, 16, pucStr);
	if (0 > ret)
	{
		hioled_error("oledRefresh failed.");
	}

	return;
}
EXPORT_SYMBOL(oledCharDisplay);

