/*****************************************************************************
  Function    : oledChgTaskFunc
  Description : oledChgTaskFunc
  Calls       : 
  Called By   : 
  Input       : NULL
  Output      : NULL
  Return      : OK              ERROR      
*****************************************************************************/
static BSP_S32 oledChgTaskFunc(BSP_VOID *arg)
{
	BSP_S32 ret = 0;

	/* for pclint */
	if (NULL == arg)
	{
		;
	}

	for(;;)
	{
		down(&g_ulOledSemSyn);

		if (g_ulPicCount >= MAX_OFF_CHARGING_ANI)
		{
			g_ulPicCount = 0;
		}

		/*顺序显充电动画*/
		ret = oledRefresh(0, 16, OLED_X_OFFSET_128, OLED_Y_OFFSET_48, g_pucPwrOffChgAniImage[g_ulPicCount]);
		if (0 > ret)
		{
			printk("oledRefresh failed.\n");
			goto fail;
		}
		g_ulPicCount++;
	}

fail:
	return 0;
}

/*****************************************************************************
  Function    : oledPwrOffChgAniInstall
  Description : 关机充电动画注册
  Calls       : 
  Called By   : 
  Input       : NULL
  Output      : NULL
  Return      : OK              ERROR      
*****************************************************************************/
BSP_S32 oledPwrOffChgAniInstall(BSP_VOID)
{
	BSP_S32 ret = 0;

	/*创建任务循环显示充电动画*/
	charge_task = kthread_run(oledChgTaskFunc, NULL, "icc_thread");
	if (IS_ERR(charge_task))
	{
		printk("create kthread charge_task failed!\n");
		return -1;
	}

	/*启动充电动画*/
	ret = queue_delayed_work(oled_queue, &oled_pwroff_chg_work, msecs_to_jiffies(1000));
	if (0 == ret)
	{
		printk("queue delayed work failed.\n");
		return ret;
	}
	
	oled_poweroff_init_flag = 1;
	
	return OK;
}

