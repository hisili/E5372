/*************************************************
*  Function:  oledRefresh
*  Description: oled refresh *
*  Called By:AP
*  Table Accessed:
*  Table Updated:
*  Input:
*          UINT32 ulXStart, UINT32 ulYStart－－起始坐标
*          UINT32 ulXOffset, UINT32 ulYOffset－－横向纵向偏移量
*          UINT8 *pucBuffer－－显示数据

*  Output:
*         N/A
*  Return:
*         N/A
*************************************************/
BSP_S32 oledRefresh(UINT32 ulXStart, UINT32 ulYStart, UINT32 ulXOffset, UINT32 ulYOffset, UINT8 *pucBuffer)
{
	UINT32 ulRowAddr = 0x00;
	UINT32 ulXloop = 0x00;
	UINT32 ulYloop = 0x00;
	UINT32 ulLoopMax = 0x00;
	UINT32 ulXEnd = (ulXStart + ulXOffset);
	UINT32 ulYEnd = (ulYStart + ulYOffset);
	UINT32 i = 0;
	UINT32 ulLineByteNum = 0x00;
	UINT32 ulXByteOffset = 0x00;
	UINT32 ulYPageOffset = 0x00;
	UINT32 ulXBitOffset = 0x00;
	UINT8 ucDisData = 0x00;    /*待显示数据*/
	UINT8 ucTmpData = 0x00;
	UINT8 ucBitData = 0x00;
	
	/*输入参数有效性检查*/
	if (NULL == pucBuffer)
	{
	    return ERROR;
	}
	
	if ((ulXEnd > OLED_WIDTH) || (ulYEnd > OLED_HIGHT) || (0 != (ulYStart % 8)) || (0 != (ulYEnd % 8)))
	{
		printk("oledRefresh para error!! \r\n");
		return ERROR;
	}
	
	/*计算页起始地址0~7*/
	ulRowAddr = ulYStart >> 3;
	    
	/*需添加互斥信号量保护*/
	down(&g_ulOledSemMux);

	/* Enable the EMI clock gating */
	/* clock_gating_enable(CLOCK_GATING_EMI); */

	/*closed display*/
	/* oledCmdOut(0xAE); */
	ulLoopMax = (ulYEnd>>3);
	for(ulYloop = ulRowAddr; ulYloop < ulLoopMax; ulYloop++)/* 按页写*/
	{
		/*设置页起始地址*/
		oledCmdOut(0x22);
		oledCmdOut((UINT8)ulYloop);
		oledCmdOut(0x07);
		
		/*设置列起始地址0~127*/
		oledCmdOut(0x21);
		oledCmdOut((UINT8)ulXStart);
		oledCmdOut(0x7f);
		ulYPageOffset = (ulYloop -ulRowAddr) ;
		ulLineByteNum = (ulXOffset >>3);
		/*X方向写入*/
		for(ulXloop = ulXStart; ulXloop < ulXEnd; ulXloop++) 
		{
			ulXByteOffset = (ulXloop -ulXStart) >> 3;
			ulXBitOffset = ((ulXloop -ulXStart) % 8) ;
			/* 将横向数据转换为纵向数据 */
			for(i = 0; i < 8; i++)
			{
				ucTmpData = *(UINT8 *)(&pucBuffer[ulYPageOffset *(ulLineByteNum)*8+i*ulLineByteNum + ulXByteOffset]);
				ucBitData = ((ucTmpData & (0x80 >> (ulXBitOffset&7)))) ? 1 : 0;
				ucDisData |= (UINT8)(ucBitData << i);//pclint 734
			}

			oledDataOut(ucDisData);
			
			ucDisData = 0;
			ucBitData = 0;
		}
	}

	/*output follows RAM content*/
	oledCmdOut(0xA4); 
	
	/*open display*/
	oledCmdOut(0xAF);
	
	/* disable the EMI clock gating */
	/* clock_gating_disable(CLOCK_GATING_EMI); */

	up(&g_ulOledSemMux);

	return OK;   
}/*lint !e550*/

